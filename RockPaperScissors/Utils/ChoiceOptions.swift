import Foundation

enum ChoiceOption: String, CaseIterable {
	case Rock = "rock"
	case Paper = "paper"
	case Scissors = "scissors"
	case Lizard = "lizard"
	case Spock = "spock"
}


enum AltChoiceOption: String, CaseIterable {
	case Rock = "rockAlt"
	case Paper = "paperAlt"
	case Scissors = "scissorsAlt"
	case Lizard = "lizardAlt"
	case Spock = "spockAlt"
}
